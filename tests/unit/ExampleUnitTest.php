namespace hopstudios\hopdocusigntests\unit;

use Codeception\Test\Unit;
use UnitTester;
use Craft;
use hopstudios\hopdocusign\HopDocusign;

/**
 * ExampleUnitTest
 *
 *
 * @author    Hop Studios
 * @package   HopDocusign
 * @since     1.0.0
 */
class ExampleUnitTest extends Unit
{
    // Properties
    // =========================================================================

    /**
     * @var UnitTester
     */
    protected $tester;

    // Public methods
    // =========================================================================

    // Tests
    // =========================================================================

    /**
     *
     */
    public function testPluginInstance()
    {
        $this->assertInstanceOf(
            HopDocusign::class,
            HopDocusign::$plugin
        );
    }

    /**
     *
     */
    public function testCraftEdition()
    {
        Craft::$app->setEdition(Craft::Pro);

        $this->assertSame(
            Craft::Pro,
            Craft::$app->getEdition()
        );
    }
}
